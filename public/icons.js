// Helper function to get icon HTML
window.getIcon = function(name, className = '') {
    return window.IconSystem.renderIcon(name, className);
};

// Auto-replace Font Awesome icons with SVG icons
document.addEventListener('DOMContentLoaded', function() {
    const iconMappings = {
        'fas fa-chart-line': 'dashboard',
        'fas fa-list': 'list',
        'fas fa-sign-out-alt': 'logout',
        'fas fa-plus': 'plus',
        'fas fa-eye': 'eye',
        'fas fa-edit': 'edit',
        'fas fa-trash': 'trash',
        'fas fa-check': 'check',
        'fas fa-times': 'x',
        'fas fa-tshirt': 'shirt',
        'fas fa-user': 'user',
        'fas fa-envelope': 'mail',
        'fas fa-phone': 'phone',
        'fas fa-lock': 'lock',
        'fas fa-info-circle': 'info',
        'fas fa-check-circle': 'success',
        'fas fa-exclamation-circle': 'warning',
        'fas fa-exclamation-triangle': 'error',
        'fas fa-save': 'save',
        'fas fa-link': 'link',
        'fas fa-envelope': 'envelope',
        'fas fa-calculator': 'calculator',
        'fas fa-spinner fa-spin': 'spinner'
    };
    
    // Replace all Font Awesome icons safely by checking classList tokens
    try {
        const allFaIcons = Array.from(document.querySelectorAll('i.fas, i.far, i.fab'));
        Object.keys(iconMappings).forEach(faClasses => {
            const required = faClasses.split(' ').filter(Boolean);
            allFaIcons.forEach(element => {
                const matches = required.every(cls => element.classList.contains(cls));
                if (matches) {
                    const iconName = iconMappings[faClasses];
                    element.outerHTML = window.getIcon(iconName, element.className);
                }
            });
        });
    } catch (e) {
        // Fail silently to avoid breaking page load
        console.error('Icon replacement error:', e);
    }
});

